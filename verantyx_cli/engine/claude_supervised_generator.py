    def 高品質コマンドを生成(self, 対象リスト: List[str]) -> int:
        """
        Claude監修のルールに基づき高品質コマンドを生成

        Args:
            対象リスト: 対象となる名詞のリスト

        Returns:
            生成したコマンド数
        """
        生成数 = 0

        for カテゴリ名, 動詞辞書 in self.動詞意味定義.items():
            for 動詞, 定義 in 動詞辞書.items():
                # 基本コマンド
                self._コマンドを登録(
                    動詞,
                    カテゴリ名,
                    定義["基本義"],
                    定義["用法パターン"],
                    定義["例文テンプレート"][0] if 定義["例文テンプレート"] else "",
                    定義["Cross軸基本"],
                    定義.get("類義語", {}),
                    定義.get("対義語", [])
                )
                生成数 += 1

                # 強度変化バリエーション
                for 強度, 動詞リスト in 定義.get("強度変化", {}).items():
                    for 変化動詞 in 動詞リスト:
                        if 変化動詞 != 動詞:  # 基本形は既に登録済み
                            self._コマンドを登録(
                                変化動詞,
                                カテゴリ名,
                                f"{定義['基本義']}({強度}強度)",
                                定義["用法パターン"],
                                "",
                                定義["Cross軸基本"],
                                {},
                                []
                            )
                            生成数 += 1

                # 対象との組み合わせ
                for 対象 in 対象リスト:
                    for パターン in 定義["用法パターン"]:
                        if "〜を" in パターン:
                            コマンド = f"{対象}を{動詞}"
                        elif "〜について" in パターン:
                            コマンド = f"{対象}について{動詞}"
                        elif "〜に" in パターン:
                            コマンド = f"{対象}に{動詞}"
                        else:
                            コマンド = f"{対象}{動詞}"

                        Cross軸 = 定義["Cross軸基本"].copy()
                        Cross軸["対象"] = 対象

                        self._コマンドを登録(
                            コマンド,
                            カテゴリ名,
                            f"{対象}に対して{定義['基本義']}",
                            [コマンド],
                            "",
                            Cross軸,
                            {},
                            []
                        )
                        生成数 += 1

        return 生成数

