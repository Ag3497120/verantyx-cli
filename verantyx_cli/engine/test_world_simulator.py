#!/usr/bin/env python3
"""
小世界シミュレータのテスト
"""

from production_jcross_engine import 本番JCrossエンジン


def テスト():
    print("=" * 80)
    print("小世界シミュレータテスト")
    print("=" * 80)
    print()

    エンジン = 本番JCrossエンジン()

    # テスト用の学習履歴を準備
    エンジン.グローバルCross構造["学習履歴"] = [
        {
            "LEFT": {
                "元テキスト": "Pythonでデータを分析したい",
                "Cross軸統合": {
                    "UP": [{"内容": "データ分析"}]
                }
            },
            "DOWN": [{"具体": "Python分析"}],
            "BACK": [{"時刻": "2026-03-10T10:00:00"}]
        },
        {
            "LEFT": {
                "元テキスト": "エラーを修正する必要がある",
                "Cross軸統合": {
                    "UP": [{"内容": "デバッグ"}]
                }
            },
            "DOWN": [{"具体": "エラー修正"}],
            "BACK": [{"時刻": "2026-03-10T11:00:00"}]
        }
    ]

    # .jcrossコードを作成
    テストコード = """
ラベル 小世界テスト
  出力する "=== 小世界シミュレータテスト開始 ==="

  # 学習履歴から小世界を構築
  取り出す グローバルCross構造.学習履歴
  取り出す 学習履歴
  実行する 小世界を構築 学習履歴
  取り出す 小世界Cross
  実行する 小世界を表示 小世界Cross

  # パターン分析
  取り出す 小世界Cross.BACK
  取り出す BACK軸
  実行する パターン分析 BACK軸
  取り出す 分析結果
  実行する パターン分析結果を表示 分析結果

  # 因果推論
  実行する 因果推論 分析結果
  取り出す 因果関係
  実行する 因果推論結果を表示 因果関係

  # 未来予測
  実行する 未来を予測 小世界Cross
  取り出す 予測結果
  実行する 予測結果を表示 予測結果

  # シミュレーション実行
  実行する シミュレーション実行 小世界Cross "新しいデータ分析タスク"
  取り出す シミュレーション結果
  実行する シミュレーション結果を表示 シミュレーション結果

  出力する "=== 小世界シミュレータテスト完了 ==="

  返す シミュレーション結果
"""

    エンジン._jcrossコードをパース(テストコード)

    try:
        結果 = エンジン.ラベルを実行("小世界テスト")
        print()
        print(f"✅ テスト成功")
        print()
        if 結果:
            print(f"最終結果:")
            print(f"  入力: {結果.get('入力', '')}")
            print(f"  信頼度: {結果.get('信頼度', 0.0):.2f}")

    except Exception as e:
        print()
        print(f"❌ テスト失敗: {e}")
        import traceback
        traceback.print_exc()


if __name__ == "__main__":
    テスト()
