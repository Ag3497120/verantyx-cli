#!/usr/bin/env python3
"""
パターン推論のテスト
"""

from production_jcross_engine import 本番JCrossエンジン


def テスト():
    print("=" * 80)
    print("パターン推論テスト")
    print("=" * 80)
    print()

    エンジン = 本番JCrossエンジン()

    # テスト用のパターンデータベースを準備
    エンジン.グローバルCross構造["パターンDB"] = [
        {
            "元テキスト": "Pythonでデータ分析",
            "キーワード": ["Python", "データ", "分析"],
            "トピック": "データ分析",
            "意図": "学習"
        },
        {
            "元テキスト": "エラーを修正する",
            "キーワード": ["エラー", "修正"],
            "トピック": "デバッグ",
            "意図": "問題解決"
        },
        {
            "元テキスト": "パフォーマンス最適化",
            "キーワード": ["パフォーマンス", "最適化"],
            "トピック": "最適化",
            "意図": "改善"
        }
    ]

    # .jcrossコードを作成
    テストコード = """
ラベル パターン推論テスト
  出力する "=== パターン推論テスト開始 ==="

  # 新しいテキストからパターン抽出
  実行する パターンを抽出 "データ分析のパフォーマンスを改善したい"
  取り出す 新パターン
  実行する パターンを表示 新パターン

  # 類似パターン探索
  実行する 類似パターンを探索 新パターン
  取り出す 類似リスト
  実行する 類似パターンを表示 類似リスト

  # 6軸で関連パターン収集
  実行する 関連パターンを6軸で収集 新パターン
  取り出す 関連Cross
  実行する Cross構造を表示 関連Cross

  # パズルピース組み合わせ
  実行する パズルピースを組み合わせ 関連Cross
  取り出す 推論候補
  実行する 推論結果を表示 推論候補

  # 最適推論選択
  実行する 最適推論を選択 推論候補
  取り出す 最適推論
  実行する 最適推論を表示 最適推論

  出力する "=== パターン推論テスト完了 ==="

  返す 最適推論
"""

    エンジン._jcrossコードをパース(テストコード)

    try:
        結果 = エンジン.ラベルを実行("パターン推論テスト")
        print()
        print(f"✅ テスト成功")
        print()
        if 結果:
            print(f"最終結果:")
            print(f"  推論: {結果.get('推論', '')}")
            print(f"  信頼度: {結果.get('信頼度', 0.0):.2f}")

    except Exception as e:
        print()
        print(f"❌ テスト失敗: {e}")
        import traceback
        traceback.print_exc()


if __name__ == "__main__":
    テスト()
