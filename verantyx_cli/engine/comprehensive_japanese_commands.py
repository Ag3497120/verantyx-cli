        self.コマンド辞書[コマンド名] = {
            "カテゴリ": カテゴリ,
            "Cross軸配置": Cross軸配置,
            "意味": 意味,
            "類義語": 類義語 or [],
            "登録時刻": datetime.now().isoformat()
        }

